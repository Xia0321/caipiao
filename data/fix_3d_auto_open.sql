-- ============================================================
-- 修复 3D (gid=252) 无法自动开盘
-- 原因：x_web.times 中 252 的 io=0，导致 pan.inc.php 每次加载都强制关盘
-- 执行方式：在数据库中执行本 SQL（或通过后台「开奖管理」- 时间设置 把 252 的「允许开盘」打开）
-- ============================================================

-- 1) 将 252 的 times 项由 io=0 改为 io=1（允许自动开盘）
-- 只替换 252 对应的 io 值，不依赖具体 key
UPDATE `x_web`
SET `times` = REPLACE(`times`, '"gid":"252","o":"0","c":"0","io":"0"', '"gid":"252","o":"0","c":"0","io":"1"')
WHERE `wid` = 100
  AND `times` LIKE '%"gid":"252"%"io":"0"%';

-- 2) 确保 3D 游戏开启自动开盘
UPDATE `x_game`
SET `autoopenpan` = 1
WHERE `gid` = 252;
